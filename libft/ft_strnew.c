#include <stdlib.h>
#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*m;

	if (!(size + 1))
		return (0);
	if (!(m = (char *)ft_memalloc(size + 1)))
		return (0);
	return (m);
}
