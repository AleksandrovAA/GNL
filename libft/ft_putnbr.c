#include "libft.h"

void	ft_putnbr(int n)
{
	char	*min;
	size_t	i;

	i = 0;
	min = "-2147483648";
	if (n == -2147483648)
		while (min[i] != '\0')
			ft_putchar(min[i++]);
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
