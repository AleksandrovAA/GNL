#include "libft.h"

char	*ft_strncat(char *st1, const char *append, size_t n)
{
	char *s1;

	s1 = (char *)st1;
	while (*s1 != '\0')
		s1++;
	while (*append != '\0' && n--)
		*s1++ = *append++;
	*s1 = '\0';
	return (st1);
}
