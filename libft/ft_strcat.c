#include "libft.h"

char	*ft_strcat(char *st1, const char *s2)
{
	char *s1;

	s1 = (char *)st1;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0')
		*s1++ = *s2++;
	*s1 = '\0';
	return (st1);
}
